#include<iostream>
using namespace std;
// switch case
int main(){
	int num = 3;
	cout << endl;
	switch( num ){
		case 1: cout << " First " << endl;
				break;
		case 2: cout << " Second " << endl;
				break;
		default: cout << " it is a default case " << endl;
		
	}
	cout << endl;
	
	
	return 0;
}